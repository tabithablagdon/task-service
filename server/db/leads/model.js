'use strict'

/************************************************************************
 * External Dependencies
 ***********************************************************************/
const mongoose              = require('mongoose')

/************************************************************************
 * Schema
 ***********************************************************************/
const Schema                = mongoose.Schema

const LeadSchema    = Schema({

    email               : {
        type                : String,
        required            : true
    },

    creation_timestamp  : {
        type                : Date,
        required            : true,
        default             : Date.now,
    }

})

/************************************************************************
 * Indexes
 ***********************************************************************/
LeadSchema.index({'creation_timestamp': 1})

/************************************************************************
 * Middleware
 ***********************************************************************/



/************************************************************************
 * Export Model
 ***********************************************************************/
module.exports = mongoose.model('Lead', LeadSchema)
